let blueScore = 5;

// Write an "if/else" statement below to print either -
// "Red has won!"
// "Blue has won!"
// "It is a draw"
if (redScore > blueScore) {
  console.log("Red has won!");
} else if (redScore < blueScore) {
  console.log("Blue has won!");
} else {
  console.log("It is a draw!");
}
